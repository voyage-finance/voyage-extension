import { utils } from 'ethers';

export const truncate = (address?: string) => {
  if (!address) return '';
  const head = address.slice(2, 6);
  const tail = address.slice(38);
  return `0x${head}...${tail}`;
};

export function emojiEncodeAddress() {
  var byteArray = utils.randomBytes(6);
  var emojiArray = [];

  for (const byte of byteArray) {
    var emoji = DEVICE_EMOJIS[byte];

    if (!emoji) {
      console.error(byte);
    }

    emojiArray.push(emoji);
  }

  return emojiArray.join('');
}

